/**
 * Script tự động quét cấu trúc docs và tạo sidebar config
 * Chạy: node generate-sidebar.js
 */

import fs from 'fs'
import path from 'path'

const docsDir = 'docs'

// Bỏ qua thư mục này
const ignoreDirs = ['images', 'node_modules', '.git']

function scanDir(dir, basePath = '') {
  const items = fs.readdirSync(path.join(docsDir, dir), { withFileTypes: true })
    .sort((a, b) => a.name.localeCompare(b.name, 'vi', { sensitivity: 'base' }))

  const result = []

  for (const item of items) {
    // Bỏ qua thư mục images
    if (ignoreDirs.includes(item.name)) continue

    const fullPath = path.join(docsDir, dir, item.name)
    const relativePath = path.join(basePath, item.name)

    if (item.isDirectory()) {
      // Đệ quy vào thư mục con
      const children = scanDir(path.join(dir, item.name), path.join(basePath, item.name))
      if (children.length > 0) {
        result.push({
          text: item.name,
          items: children
        })
      }
    } else if (item.isFile() && item.name.endsWith('.md')) {
      // Bỏ qua file index.md (trang chủ VitePress, không cần trong sidebar)
      if (item.name === 'index.md') continue

      const nameWithoutExt = item.name.replace('.md', '')
      const link = `/${relativePath.replace('.md', '')}`
      const title = nameWithoutExt

      result.push({
        text: title,
        link: link
      })
    }
  }

  return result
}

function generateSidebar() {
  const sidebarConfig = scanDir('')

  const output = `// Auto-generated by generate-sidebar.js
// DO NOT EDIT MANUALLY

export const autoSidebar = ${JSON.stringify(sidebarConfig, null, 2)}

export default autoSidebar
`

  fs.writeFileSync('sidebar-auto.js', output)
  console.log('✅ Đã tạo sidebar-auto.js')
  console.log(JSON.stringify(sidebarConfig, null, 2))
}

generateSidebar()
